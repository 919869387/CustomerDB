package pojo;


public class ManageCustomer {
	
	int id;
	String managerid;
	String managername;
	String customerid;
	String managercustomerrelation;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getManagerid() {
		return managerid;
	}
	public void setManagerid(String managerid) {
		this.managerid = managerid;
	}
	public String getCustomerid() {
		return customerid;
	}
	public void setCustomerid(String customerid) {
		this.customerid = customerid;
	}
	public String getManagername() {
		return managername;
	}
	public void setManagername(String managername) {
		this.managername = managername;
	}
	public String getManagercustomerrelation() {
		return managercustomerrelation;
	}
	public void setManagercustomerrelation(String managercustomerrelation) {
		this.managercustomerrelation = managercustomerrelation;
	}
	
}
